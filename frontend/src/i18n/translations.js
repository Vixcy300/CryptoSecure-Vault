// Get translation for current language
export const t = (key, lang = 'en') => {
    return translations[lang]?.[key] || translations.en[key] || key;
};

// Hook for React components
export const useTranslation = (lang = 'en') => {
    return {
        t: (key) => t(key, lang),
        lang
    };
};

